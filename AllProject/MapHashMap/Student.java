package MapHashMap;

public class Student {
    private  String name;
    private int no;
    private int note;


    public Student(String name, int no, int note) {
        this.name = name;
        this.no = no;
        this.note = note;
    }

    public String getName() {
        return name;
    }

    public int getNo() {
        return no;
    }

    public int getNote() {
        return note;
    }
}
